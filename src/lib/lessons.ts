export function getLessonById(id: string): Lesson | undefined {
  return lessons.find((l) => l.id === id);
}

export function getLessonsByModule(module: number): Lesson[] {
  return lessons.filter((l) => l.module === module);
}

export function getNextLesson(currentId: string): Lesson | undefined {
  const index = lessons.findIndex((l) => l.id === currentId);
  if (index === -1 || index >= lessons.length - 1) return undefined;
  return lessons[index + 1];
}

export function getPrevLesson(currentId: string): Lesson | undefined {
  const index = lessons.findIndex((l) => l.id === currentId);
  if (index <= 0) return undefined;
  return lessons[index - 1];
}


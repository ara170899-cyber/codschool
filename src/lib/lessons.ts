export function getLessonModuleInfo(lessonId: string): {
  lessonIndex: number;
  totalInModule: number;
  moduleName: string;
} | undefined {
  const lesson = getLessonById(lessonId);
  if (!lesson) return undefined;
  const moduleLessons = getLessonsByModule(lesson.module);
  const lessonIndex = moduleLessons.findIndex((l) => l.id === lessonId);
  return {
    lessonIndex: lessonIndex + 1,
    totalInModule: moduleLessons.length,
    moduleName: getModuleName(lesson.module),
  };
}
